using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;

namespace ExpenseProcessingSystem
{
    public class SampleClassForTesting
    {
        public float ReturnSum(float num1, float num2)
        {
            return (num1 + num2);
        }
        public float ReturnDiff(float num1, float num2)
        {
            return (num1 - num2);
        }
        public float ReturnProd(float num1, float num2)
        {
            return (num1 * num2);
        }
        public float ReturnQou(float num1, float num2)
        {
            return (num1 / num2);
        }
    }
}
